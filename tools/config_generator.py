def print_usage():
    print("""
Usage: python3 config_generator.py [options]

Options:
  -i, --interactive     Create config interactively
  -f, --file FILE       Load config from JSON file
  -g, --generate        Generate C code
  -o, --output DIR      Output directory (default: generated)
  -s, --save FILE       Save config to JSON file
  --example             Create example config file
  -h, --help            Show this help

Examples:
  python3 config_generator.py --example --save my_config.json
  python3 config_generator.py -f my_config.json
  python3 config_generator.py -f my_config.json -g -o output/
  python3 config_generator.py -i -s new_config.json -g
""")


def main():
    # Simple argument parsing (no argparse to keep it simple)
    args = sys.argv[1:]
    
    if not args or '-h' in args or '--help' in args:
        print_usage()
        return 0
    
    config = None
    output_dir = 'generated'
    do_generate = False
    save_file = None
    
    i = 0
    while i < len(args):
        arg = args[i]
        
        if arg in ['-i', '--interactive']:
            config = interactive_mode()
        
        elif arg in ['-f', '--file']:
            i += 1
            if i < len(args):
                config = load_config(args[i])
                print(f"Loaded: {args[i]}")
        
        elif arg in ['-g', '--generate']:
            do_generate = True
        
        elif arg in ['-o', '--output']:
            i += 1
            if i < len(args):
                output_dir = args[i]
        
        elif arg in ['-s', '--save']:
            i += 1
            if i < len(args):
                save_file = args[i]
        
        elif arg == '--example':
            config = create_example()
            print("Created example configuration")
        
        i += 1
    
    if config is None:
        print("Error: No configuration loaded. Use -f or -i or --example")
        return 1
    
    # Save if requested
    if save_file:
        save_config(config, save_file)
        print(f"Saved: {save_file}")
    
    # Always run analysis
    is_valid = print_analysis(config)
    
    # Generate code if requested and valid
    if do_generate:
        if is_valid:
            print("Generating C code...")
            generate_code(config, output_dir)
            print("Done!")
        else:
            print("Cannot generate code - fix errors first")
            return 1
    
    return 0


if __name__ == '__main__':
    sys.exit(main())
